<script>
export default {
    computed: {
        currentDate() {
            return new Date().getDate();
        },
        currentDayOfWeek() {
            let day = new Date().getDay();
            return this.getDayOfWeek(day);
        },
        currentMonth() {
            let currentMonth = new Date().getMonth();
            return this.getMonth(currentMonth);
        },
    },
    methods: {
        getDayOfWeek(day) {
            switch (day) {
                case 0:
                    return "Неділя";
                case 1:
                    return "Понеділок";
                case 2:
                    return "Вівторок";
                case 3:
                    return "Середа";
                case 4:
                    return "Четвер";
                case 5:
                    return "П'ятниця";
                case 6:
                    return "Субота";
            }
        },
        getMonth(month) {
            const months = [
                "Січень",
                "Лютий",
                "Березень",
                "Квітень",
                "Травень",
                "Червень",
                "Липень",
                "Серпень",
                "Вересень",
                "Жовтень",
                "Листопад",
                "Грудень",
            ];
            return months[month];
        },
    },
};
</script>
